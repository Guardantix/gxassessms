# ---------------------------------------------------------------------------
# Rollback: directory provision failure
# ---------------------------------------------------------------------------


class TestEngagementCreateDirProvisionRollback:
    """create_engagement_dir failure -> DB row deleted, exit nonzero."""

    @patch("gxassessms.cli._helpers.get_artifact_manager", autospec=True)
    @patch("gxassessms.cli._helpers.get_engagement_repo", autospec=True)
    def test_dir_failure_deletes_db_row(
        self,
        mock_get_repo: MagicMock,
        mock_get_artifacts: MagicMock,
        tmp_path: Path,
    ) -> None:
        """When create_engagement_dir raises, repo.delete(eid) is called."""
        config_path = _write_config(tmp_path)
        mock_repo = _make_mock_repo("eng-rollback-dir")
        mock_get_repo.return_value = mock_repo
        mock_artifacts = MagicMock()
        mock_artifacts.create_engagement_dir.side_effect = OSError("disk full")
        mock_get_artifacts.return_value = mock_artifacts

        runner = CliRunner()
        result = runner.invoke(cli, ["engagement", "create", str(config_path)])

        assert result.exit_code != 0
        actual_id = mock_repo.create.call_args.kwargs["engagement_id"]
        mock_repo.delete.assert_called_once_with(actual_id)

    @patch("gxassessms.cli._helpers.get_artifact_manager", autospec=True)
    @patch("gxassessms.cli._helpers.get_engagement_repo", autospec=True)
    def test_dir_failure_exits_nonzero(
        self,
        mock_get_repo: MagicMock,
        mock_get_artifacts: MagicMock,
        tmp_path: Path,
    ) -> None:
        """When create_engagement_dir raises, exit code is nonzero."""
        config_path = _write_config(tmp_path)
        mock_get_repo.return_value = _make_mock_repo("eng-rollback-dir-exit")
        mock_artifacts = MagicMock()
        mock_artifacts.create_engagement_dir.side_effect = OSError("disk full")
        mock_get_artifacts.return_value = mock_artifacts

        runner = CliRunner()
        result = runner.invoke(cli, ["engagement", "create", str(config_path)])

        assert result.exit_code != 0

    @patch("gxassessms.cli._helpers.get_artifact_manager", autospec=True)
    @patch("gxassessms.cli._helpers.get_engagement_repo", autospec=True)
    def test_dir_failure_rollback_delete_failure_does_not_reraise(
        self,
        mock_get_repo: MagicMock,
        mock_get_artifacts: MagicMock,
        tmp_path: Path,
    ) -> None:
        """If rollback delete itself raises, it is swallowed (best-effort)."""
        from gxassessms.core.contracts.errors import PersistenceError

        config_path = _write_config(tmp_path)
        mock_repo = _make_mock_repo("eng-rollback-del-fail")
        mock_repo.delete.side_effect = PersistenceError("gone already")
        mock_get_repo.return_value = mock_repo
        mock_artifacts = MagicMock()
        mock_artifacts.create_engagement_dir.side_effect = OSError("disk full")
        mock_get_artifacts.return_value = mock_artifacts

        runner = CliRunner()
        # Should not raise -- just exit nonzero
        result = runner.invoke(cli, ["engagement", "create", str(config_path)])
        assert result.exit_code != 0


