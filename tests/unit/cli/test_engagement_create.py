"""Tests for engagement create bootstrap provisioning.

Verifies the full three-artifact bootstrap:
  1. DB row created with pre-generated engagement_id
  2. On-disk engagement directory provisioned via ArtifactManager
  3. Config snapshot mirrored to filesystem

And rollback invariants:
  - Dir provision failure -> DB row deleted
  - update_engagement_dir failure -> dir removed + DB row deleted
  - Mirror failure -> dir removed + DB row deleted
"""

from __future__ import annotations

from pathlib import Path
from unittest.mock import MagicMock, patch

import yaml
from click.testing import CliRunner

from gxassessms.cli.main import cli

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _write_config(tmp_path: Path) -> Path:
    """Write a minimal valid config YAML and return its path."""
    config_path = tmp_path / "config.yaml"
    config_data = {
        "client": {
            "name": "Test Corp",
            "tenant_id": "00000000-0000-0000-0000-000000000001",
        },
        "auth": {
            "method": "client_credential",
            "tenant_id": "00000000-0000-0000-0000-000000000001",
            "client_id": "00000000-0000-0000-0000-000000000002",
            "client_secret_env": "GX_SECRET",  # pragma: allowlist secret
        },
        "tools": {
            "scubagear": True,
        },
    }
    config_path.write_text(yaml.dump(config_data), encoding="utf-8")
    return config_path


def _make_mock_repo(engagement_id: str = "eng-bootstrap-001") -> MagicMock:
    """Return a mock EngagementRepo whose create() echoes back the engagement_id kwarg.

    The engagement_id is pre-generated by the CLI and passed in; create() returns it
    unchanged. The engagement_id parameter here is kept for API compatibility but is
    no longer used as a fixed return value.
    """
    repo = MagicMock()
    repo.create.side_effect = lambda **kwargs: kwargs.get("engagement_id", engagement_id)
    return repo


def _make_mock_artifacts(eng_dir: Path | None = None) -> MagicMock:
    """Return a mock ArtifactManager whose create_engagement_dir() returns eng_dir."""
    artifacts = MagicMock()
    if eng_dir is not None:
        artifacts.create_engagement_dir.return_value = eng_dir
    return artifacts


