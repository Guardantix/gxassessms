class TestEngagementCreateMirrorRollback:
    """Mirror failure -> dir removed + DB row deleted, exit nonzero."""

    @patch(
        "gxassessms.pipeline.config_snapshot_mirror.mirror_config_snapshot_from_db_strict",
        autospec=True,
    )
    @patch("gxassessms.cli._helpers.get_artifact_manager", autospec=True)
    @patch("gxassessms.cli._helpers.get_engagement_repo", autospec=True)
    def test_mirror_failure_removes_dir_and_deletes_row(
        self,
        mock_get_repo: MagicMock,
        mock_get_artifacts: MagicMock,
        mock_mirror: MagicMock,
        tmp_path: Path,
    ) -> None:
        """When mirror raises, shutil.rmtree and repo.delete are called."""
        from gxassessms.core.contracts.errors import ConfigSnapshotMirrorError

        config_path = _write_config(tmp_path)
        eng_dir = tmp_path / "test-corp-eng-mirror"
        eng_dir.mkdir()
        mock_repo = _make_mock_repo("eng-mirror-rollback")
        mock_get_repo.return_value = mock_repo
        mock_artifacts = _make_mock_artifacts(eng_dir)
        mock_get_artifacts.return_value = mock_artifacts
        mock_mirror.side_effect = ConfigSnapshotMirrorError(
            "write failed", engagement_id="eng-mirror-rollback"
        )

        with patch("shutil.rmtree") as mock_rmtree:
            runner = CliRunner()
            result = runner.invoke(cli, ["engagement", "create", str(config_path)])

        assert result.exit_code != 0
        mock_rmtree.assert_called_once_with(eng_dir, ignore_errors=True)
        actual_id = mock_repo.create.call_args.kwargs["engagement_id"]
        mock_repo.delete.assert_called_once_with(actual_id)

    @patch(
        "gxassessms.pipeline.config_snapshot_mirror.mirror_config_snapshot_from_db_strict",
        autospec=True,
    )
    @patch("gxassessms.cli._helpers.get_artifact_manager", autospec=True)
    @patch("gxassessms.cli._helpers.get_engagement_repo", autospec=True)
    def test_mirror_failure_exits_nonzero(
        self,
        mock_get_repo: MagicMock,
        mock_get_artifacts: MagicMock,
        mock_mirror: MagicMock,
        tmp_path: Path,
    ) -> None:
        """Mirror failure exits nonzero."""
        from gxassessms.core.contracts.errors import ConfigSnapshotMirrorError

        config_path = _write_config(tmp_path)
        eng_dir = tmp_path / "test-corp-eng-mirror-exit"
        eng_dir.mkdir()
        mock_get_repo.return_value = _make_mock_repo("eng-mirror-exit")
        mock_get_artifacts.return_value = _make_mock_artifacts(eng_dir)
        mock_mirror.side_effect = ConfigSnapshotMirrorError(
            "write failed", engagement_id="eng-mirror-exit"
        )

        runner = CliRunner()
        result = runner.invoke(cli, ["engagement", "create", str(config_path)])

        assert result.exit_code != 0

    @patch(
        "gxassessms.pipeline.config_snapshot_mirror.mirror_config_snapshot_from_db_strict",
        autospec=True,
    )
    @patch("gxassessms.cli._helpers.get_artifact_manager", autospec=True)
    @patch("gxassessms.cli._helpers.get_engagement_repo", autospec=True)
    def test_mirror_failure_rollback_delete_failure_does_not_reraise(
        self,
        mock_get_repo: MagicMock,
        mock_get_artifacts: MagicMock,
        mock_mirror: MagicMock,
        tmp_path: Path,
    ) -> None:
        """If rollback delete raises during mirror failure, it is swallowed."""
        from gxassessms.core.contracts.errors import (
            ConfigSnapshotMirrorError,
            PersistenceError,
        )

        config_path = _write_config(tmp_path)
        eng_dir = tmp_path / "test-corp-eng-mirror-del-fail"
        eng_dir.mkdir()
        mock_repo = _make_mock_repo("eng-mirror-del-fail")
        mock_repo.delete.side_effect = PersistenceError("already gone")
        mock_get_repo.return_value = mock_repo
        mock_get_artifacts.return_value = _make_mock_artifacts(eng_dir)
        mock_mirror.side_effect = ConfigSnapshotMirrorError(
            "write failed", engagement_id="eng-mirror-del-fail"
        )

        runner = CliRunner()
        result = runner.invoke(cli, ["engagement", "create", str(config_path)])
        assert result.exit_code != 0
